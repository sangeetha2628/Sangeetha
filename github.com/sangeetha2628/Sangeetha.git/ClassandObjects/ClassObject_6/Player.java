package ClassObject_6;

public class Player {
private String pn;
private String pp;
public Player(String pn, String pp) {
	super();
	this.pn = pn;
	this.pp = pp;
}
public String getpn() {
	return pn;
}

	
public void setpn(String pn) {
	this.pn=pn;
}

public String getpp() {
	return pp;
}

	
public void setpp(String pp) {
	this.pp=pp;
}
}



